package com.shibeta.unit4.day02;

/**
 * Math
 */
public class Demo2_Math {
    public static void main(String[] args) {
        System.out.println(Math.abs(-100));
        System.out.println(Math.max(120, 1));
        System.out.println(Math.min(100, 1));
        // 四舍五入
        System.out.println(Math.round(199.5));
        System.out.println(Math.round(-199.5));
        // 返回小于等于参数的最大整数
        System.out.println(Math.floor(3.8));
        System.out.println(Math.floor(-3.8));
        // 返回大于等于参数的最小整数
        System.out.println(Math.ceil(3.8));
        System.out.println(Math.ceil(-3.8));



    }
}
