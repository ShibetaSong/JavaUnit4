package com.shibeta.unit4.day08.Demo2;

public class kjj {
    // 快捷键
    public static void main(String[] args) {
        /*
        提示与代码生成
        alt + enter 根据光标所在位置，提供快速修复选项
        ctrl + alt + t 对选中的代码弹出环绕选项(try、if等)
        alt + insert 代码自动生成，构造函数等
        ctrl + o    选择可重写的方法
        ctrl + i    选择可实现的方法
         */

        /*
        选择/移动/复制
        ctrl + w 递进式选择
        ctrl + shift + w 递进式取消选择
        ctrl + shift + 箭头 移动当前行或代码结构
        alt + shift + 箭头 移动当前行
        ctrl + y 删除光标所在行 或 删除选中的行
        ctrl + x 剪切光标所在行 或 选中的行
         */

        /*
        代码优化/ 查找与替换
        ctrl + alt + o 优化导入的类
        ctrl + alt + l 格式化代码
        ctrl + f 在当前文件内查找
        ctrl + shift + f 在整个项目 或 指定目录内文件 中查找
        ctrl + r 在当前文件进行文本替换
        ctrl + shift + r  整个项目 或 指定目录内文件中替换
         */

        /*
        阅读代码
        ctrl + q 显示光标所在的变量/类名/方法名的文档内容
        ctrl + p 方法参数提示
        ctrl + g 跳转到指定行处
        ctrl + b == ctrl + 左键
        ctrl + - 折叠代码
        ctrl + + 展开代码
        ctrl + k 版本控制提交
        ctrl + t 版本控制更新
        ctrl + [ 移动光标到所在代码的花括号开始位置
        ctrl + ] 移动光标到所在代码的花括号结束位置
        ctrl + F1 在光标所在错误代码处显示错误信息
        ctrl + F3 跳转到所选中的词的下一个引用位置
        ctrl + F8 设置光标当前行为断点/取消断点
        ctrl + alt + v 快速引进变量

        alt + 左右向箭头 切换
        shift + F2 跳转到上一个高亮错误 或 警告位置
        shift + F3 在查找模式下，查找匹配上一个
        shift + F9 等效于调试按钮
        shift + F10 等效于运行按钮
        alt + shift + F10 运行菜单
        alt + shift + F9 调试菜单

        ctrl + alt + 左右向箭头 退回/前进到上一个操作的地方
        ctrl + shift + z 取消撤销
        ctrl + shift + j 将下一行合并到当前行末尾
        ctrl + shift + u 对选中的代码进行大小写轮流转换
        ctrl + shift + / 代码块注释
        ctrl + shift + + 展开所有代码
        ctrl + shift + F8 指定断点进入条件

         */
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
            System.out.println("ABC");
        }
        System.out.println(123);
        System.out.println(123);
        System.out.println(321);
        System.out.println(123);

    }

}
